     // * Définit la largeur de la " SideBar" de 250px et la marge gauche du contennu de la page de 250px.
     function openNav() {
         document.getElementById("mySidebar").style.width = "250px";
         document.getElementById("main").style.marginLeft = "250px";
     }
 
   // * Définit la largeur de la " SideBar " à 0 et la marge gauche du contenu de la page à 0.
     function closeNav() {
       document.getElementById("mySidebar").style.width = "0";
       document.getElementById("main").style.marginLeft = "0";
     }
    